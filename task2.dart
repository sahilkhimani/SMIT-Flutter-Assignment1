//Take two variables and store age then using if/else condition to determine oldest and youngest among them.

void main() {
  int age1 = 30;
  int age2 = 25;

  print("Age of First Person is: ${age1}");
  print("Age of Second Person is: ${age2}");

  if (age1 > age2) {
    print("The first person is Oldest");
  } else {
    print("The first person is youngest");
  }
}
